package oops.pen;

public class Ink {
	String colorOfInk;
	int quantityOfInk;

	public String getColorOfInk() {
		return colorOfInk;
	}

	public void setColorOfInk(String colorOfInk) {
		this.colorOfInk = colorOfInk;
	}

	public int getQuantityOfInk() {
		return quantityOfInk;
	}

	public void setQuantityOfInk(int quantityOfInk) {
		this.quantityOfInk = quantityOfInk;
	}

}
